export function CountryAutocomplete({ 
  value, 
  onChange, 
  placeholder = "Start typing a country name...", 
  className,
  required = false 
}: CountryAutocompleteProps) {
  const [open, setOpen] = useState(false);
  const [inputValue, setInputValue] = useState(value);
  const [filteredCountries, setFilteredCountries] = useState<typeof COUNTRIES>([]);
  const inputRef = useRef<HTMLInputElement>(null);

  // Update input value when prop value changes
  useEffect(() => {
    setInputValue(value);
  }, [value]);

  // Filter countries based on input
  useEffect(() => {
    if (inputValue.trim()) {
      const filtered = COUNTRIES.filter(country => {
        const words = country.name.toLowerCase().split(' ');
        return words.some(word => word.startsWith(inputValue.toLowerCase()));
      }).slice(0, 10);
      setFilteredCountries(filtered);
    } else {
      setFilteredCountries([]);
    }
  }, [inputValue]);

  const handleInputChange = (newValue: string) => {
    setInputValue(newValue);
    
    // Only call onChange if the value matches exactly a country name
    const exactMatch = COUNTRIES.find(country => 
      country.name.toLowerCase() === newValue.toLowerCase()
    );
    
    if (exactMatch) {
      onChange(exactMatch.name);
    } else {
      // Clear the onChange value if no exact match
      onChange('');
    }
    
    if (newValue.trim()) {
      setOpen(true);
    }
  };

  const handleCountrySelect = (country: typeof COUNTRIES[0]) => {
    setInputValue(country.name);
    onChange(country.name);
    setOpen(false);
  };

  const handleInputFocus = () => {
    if (inputValue.trim()) {
      setOpen(true);
    }
  };

  const handleInputBlur = () => {
    // Delay closing to allow selection clicks
    setTimeout(() => setOpen(false), 200);
  };

  return (
    <div className={cn("relative", className)}>
      <Input
        ref={inputRef}
        value={inputValue}
        onChange={(e) => handleInputChange(e.target.value)}
        onFocus={handleInputFocus}
        onBlur={handleInputBlur}
        placeholder={placeholder}
        className="pr-8"
        required={required}
      />
      
      {open && filteredCountries.length > 0 && (
        <div className="absolute z-50 w-full mt-1 bg-popover border rounded-md shadow-lg max-h-60 overflow-auto">
          <ScrollArea className="max-h-60">
            <div className="p-1">
              {filteredCountries.map((country) => (
                <div
                  key={country.code}
                  className="px-2 py-1.5 text-sm cursor-pointer hover:bg-accent hover:text-accent-foreground rounded-sm"
                  onClick={() => handleCountrySelect(country)}
                >
                  {country.name}
                </div>
              ))}
            </div>
          </ScrollArea>
        </div>
      )}
    </div>
  );
}